public class VariableDeclaration {
    public static void main(String[] args) {
        int age = 28;
        System.out.println("Age: " + age);

        String firstName = "Ilyas";
        String lastName = "Faquih";
        final double PI = 3.14;
        double radius = 7.5;
        String favoriteSuperhero = "Messi";
        String favoriteQuote = "Difficult roads often lead to beautiful destinations.";

        System.out.println("First Name: " + firstName);
        System.out.println("Last Name: " + lastName);
        System.out.println("PI: " + PI);
        System.out.println("Radius: " + radius);
        System.out.println("Favorite Superhero: " + favoriteSuperhero);
        System.out.println("Favorite Quote: " + favoriteQuote);
    }
}
